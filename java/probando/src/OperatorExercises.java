public class OperatorExercises {
    public static void main(String[] args){
        int a = 2;
        int b = 4;
        int c = 6;
        int d = 6;

        System.out.println(a<b);
        System.out.println(c==d);
        System.out.println(c>b);

        System.out.println(a<b && c==d);

        System.out.println(++a);
    }


}
